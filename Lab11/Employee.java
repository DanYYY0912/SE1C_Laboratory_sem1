public class Employee {
    private String name;
    private int salary;
    
    public String getName() 
    {
        return name;
    }
    
    public void setName(String EmpName) 
    {
        name = EmpName;
    }
    
    public int getSalary()
    {
        return salary;
    }
    
    public void setSalary(int EmpSal) 
    {
        salary = EmpSal;
    }
}
